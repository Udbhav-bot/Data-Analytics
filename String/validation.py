a=input('Enter Anything: ')
if a.isalpha():
    print("You entered Alphabets")
if a.isalnum():
    print("You entered Alphanumerics")
if a.isdigit():
    print("You entered Digits")
if a.isnumeric():
    print("You entered Numbers")
if a.islower():
    print("You entered Lower Case")
if a.isalpha():
    print("You entered Upper Case")